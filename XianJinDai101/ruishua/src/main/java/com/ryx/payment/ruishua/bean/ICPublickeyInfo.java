package com.ryx.payment.ruishua.bean;

import java.io.Serializable;

public class ICPublickeyInfo implements Serializable {

	private static final long serialVersionUID = 1L;

	private String verNo;
	private String downFlag=""; 
	private String rid;
	private String icKeys;
	private String expiDate;
	private String timesTamp;  
	private String keyIndex;
	
	
	public String getVerNo() {
		return verNo;
	}
	public void setVerNo(String verNo) {
		this.verNo = verNo;
	}
	public String getDownFlag() {
		return downFlag;
	}
	public void setDownFlag(String downFlag) {
		this.downFlag = downFlag;
	}
	public String getRid() {
		return rid;
	}
	public void setRid(String rid) {
		this.rid = rid;
	}
	public String getIcKeys() {
		return icKeys;
	}
	public void setIcKeys(String icKeys) {
		this.icKeys = icKeys;
	}
	public String getExpiDate() {
		return expiDate;
	}
	public void setExpiDate(String expiDate) {
		this.expiDate = expiDate;
	}
	public String getTimesTamp() {
		return timesTamp;
	}
	public void setTimesTamp(String timesTamp) {
		this.timesTamp = timesTamp;
	}
	public String getKeyIndex() {
		return keyIndex;
	}
	public void setKeyIndex(String keyIndex) {
		this.keyIndex = keyIndex;
	}
		
	
	
	
}
