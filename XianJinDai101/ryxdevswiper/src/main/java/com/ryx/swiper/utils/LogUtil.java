import android.util.Log;

/**
 * 日志工具包
 */
public class LogUtil {

    /**
     * 打印普通信息
     *
     * @param tag
     * @param msg
     */

    public static boolean logdebug=true;

    public static void setLogdebug(boolean logdebug) {
        LogUtil.logdebug = logdebug;
    }

    public static void printInfo(String msg) {
        if (logdebug) {
            Log.i("testsdk", msg);
        }
    }

}
